export function buildPredictionPrompt({
  element,
  favorable_elements,
  unfavorable_elements,
  today,
  randomTopic
}) {
  return `
เขียนดวงรายวันภาษาไทยสไตล์เพจดูดวง พูดตรง กระชับ เป็นธรรมชาติ เหมือนแอดมินเพจเขียนเอง

ข้อมูลประกอบ:
วันที่ ${today}
หมวดโฟกัสวันนี้: ${randomTopic}
พลังพื้นฐาน: ${element}
ธาตุส่งเสริม: ${favorable_elements}
ธาตุควรเลี่ยง: ${unfavorable_elements}

กติกาการเขียน:
- เขียน 1 บรรทัดเดียวเท่านั้น ห้ามขึ้นบรรทัดใหม่
- ไม่เกิน 26 คำ
- ต้องขึ้นต้นด้วย "${today}"
- ต้องมีคำว่า "เรื่องเด่นวันนี้คือ" และ "แต่ต้องระวังเรื่อง"
- เรื่องเด่น = โอกาสหรือจังหวะที่ได้เปรียบ
- เรื่องระวัง = พฤติกรรมหรือเหตุการณ์ที่ทำให้พัง
- เนื้อหาต้องเกี่ยวกับหมวด ${randomTopic} อย่างชัดเจน

ให้ใช้ภาษาคนทั่วไป:
- ใช้คำกริยา เช่น คุยงาน ปิดดีล ใช้เงิน รับปาก งอน เผลอพูด ตัดสินใจช้า
- ใช้สถานการณ์ที่เห็นภาพได้จริง

ห้ามใช้:
- คำนามนามธรรม เช่น ความมั่นคง ความสำเร็จ ความไว้วางใจ โอกาสเปิดกว้าง
- โครงสร้างภาษาทางการ เช่น "การ...ที่มีความ..."
- คำเชื่อมเหตุผล เช่น เพราะ ดังนั้น จึง
- ศัพท์โหราศาสตร์
- สรรพนาม ฉัน เรา ผม ดิฉัน
- คำเว่อร์เกินจริง
- ห้ามอวยหรือชมเกินจริง เช่น สมบูรณ์แบบ ทุกด้าน ดีที่สุด
- ห้ามใช้คำว่า คุณ

รูปแบบต้องเป็นแบบนี้เท่านั้น:
${today} เรื่องเด่นวันนี้คือ... แต่ต้องระวังเรื่อง...

ถ้ายาวเกิน 26 คำ ให้ตัดท้ายทันทีโดยไม่อธิบาย
`;
}
